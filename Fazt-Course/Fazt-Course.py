# Lenguaje de programacion de Proposito General, No tiene proposito definido (Ingenieria de Datos)
# Lenguaje de Alto nivel y Dinamico

# FUNCION
print("Hello World")


# TIPOS DE DATOS
# TIPOS DE DATOS

# STRINGS
print("Hello World")
print('Hello World')
print("""Hello World""")
# Type defino el tipo de dato
print(type("Hello World"))
print(type(100))
# Uniendo String
print("Bye" + "World")
print("Bye" + "asasasassasaassasasasa")

# NUMBERS
print(30)
# int = Tipo de Dato entero
print(type(30))
# float = Tipo de Dato Decimal
print(type(30.8))

# BOOLEAN
print(True)
print(False)
# bool = Tipo de Dato Booleano
print(type(False))

# AGRUPAR DATOS
# AGRUPAR DATOS

# LISTAS
[10, 20, 30, 40]
["Hello", "World", "Bye"]
["Hello", 10, 10.5, True]
# List
print(type(["Hello", 10, 10.5, True]))
# Lista Vacia
[]

# TUPLES
# Listas de Datos que No Cambian "()"
(10, 20, 30, 40, 66)
print((10, 20, 44, 55))
print(type((10, 20, 44, 55)))
# Tuples Vacia
()

# DICCIONARIOS
# Listas de un mismo tipo de dato, con un nombre clave (Key-Value)
{
    "First_Name": "Ryan",
    "Last_Name": "Ray",
    "User_Name": "Fazt"
}
{
    "Lat": 12121222112,
    "Long": 2525252525,
}
print({
    "First_Name": "Ryan",
    "Last_Name": "Ray",
    "User_Name": "Fazt"
})
# Dict = Diccionario
print(type({
    "First_Name": "Ryan",
    "Last_Name": "Ray",
    "User_Name": "Fazt"
}))
# Tipo de Dato Vacio (Sin definicion)
None
print(type(None))
